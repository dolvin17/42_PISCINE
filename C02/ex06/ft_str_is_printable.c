#include <stdio.h>

int	ft_str_is_printable(char *str)
{
	int	i;

	i = 0;
	if (!str)
		return (1);
	while (str[i])
	{
		if (str[i] >= ' ' && str[i] <= '~')
			;
		else
			return (0);
		i++;
	}
	return (1);
}
